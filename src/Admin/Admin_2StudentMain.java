    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        tableStudent.getTableHeader().setBackground(Color.decode("#d9e1ec"));
        
        admin = new User();
        //AdminTask.initUsernameRole(admin.getUsername(), admin.getRole(), lblUsername, lblRole);
        //System.out.println("HIIIIIIII" + admin.getUsername());
        
        AdminTask task = new AdminTask();
        List<String> data = task.readtxt("student.txt");
        task.addRowtoTable(tableStudent, data, 1);
        tableStudent.getColumnModel().setColumnMargin(10);
        
        TableActionEvent event = new TableActionEvent() {
            @Override
            public void onEdit(int row) {
                //System.out.print("HIIIIII");
                String rowID = tableStudent.getModel().getValueAt(row, 1).toString();
                //System.out.println(rowID);
                
                //student
                Student student = new Student();
                student.setID(rowID);
                
                //open student edit page
                //Assignment.Admin_4StudentEdit.show();
                Admin_4StudentEdit editPage = new Admin_4StudentEdit();

                student.editStudentInit(
                    editPage.getCourseCombo(),
                    editPage.getLblID(),
                    editPage.getTxtUsername(),
                    editPage.getTxtFullName(),
                    editPage.getTxtEmail(),
                    editPage.getTxtPhone(),
                    editPage.getTxtPw()
                );

                editPage.setVisible(true);
                Admin_2StudentMain.this.dispose();
            }

            @Override
            public void onDelete(int row) {
                int result = JOptionPane.showConfirmDialog(null, "Delete student data?", "Confirmation", JOptionPane.YES_NO_OPTION);
                if (result == JOptionPane.YES_OPTION){
                    String rowID = tableStudent.getModel().getValueAt(row, 1).toString();
                    
                    Student student = new Student();
                    student.deletestudent(rowID);
                    
                    JOptionPane.showMessageDialog(null, "Student data has been successfully deleted");
                } else {
                    JOptionPane.showMessageDialog(null, "Deletion cancelled");
                }
                List<String> data = task.readtxt("student.txt");
                task.addRowtoTable(tableStudent, data, 1);
                
                Admin_2StudentMain mainPage = new Admin_2StudentMain();
                mainPage.setVisible(true);
                Admin_2StudentMain.this.dispose();
            }
        };
        tableStudent.getColumnModel().getColumn(3).setCellRenderer(new TableActionCellRender());
        tableStudent.getColumnModel().getColumn(3).setCellEditor(new TableActionCellEditor(event));
    }//GEN-LAST:event_formComponentShown

