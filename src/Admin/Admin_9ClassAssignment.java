    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String moduleID = Class.getmoduleID();
        
        String classType = (String) cmbClassType.getSelectedItem();
        String ID = txtclassID.getText();       
        String name = txtclassName.getText();
        String lecturer = txtLect.getText();
        String day = txtDay.getText();
        String startTime = txtStartTime.getText().toLowerCase();
        String duration = (String) cmbHour.getSelectedItem();
        String sizelimit = (String) cmbSizeLimit.getSelectedItem();
        String venue = (String) cmbVenue.getSelectedItem();
        
        int hour = Integer.parseInt(startTime.substring(0, startTime.indexOf(':')));
        int minute = Integer.parseInt(startTime.substring(startTime.indexOf(':') + 1));
        LocalTime time = LocalTime.of(hour, minute); // Start time
        double minduration = (Double.parseDouble(duration)) * 60;
        time = time.plusMinutes((int) minduration);
        
        //check kenapa it allows eventho color of bg supposed to be blue
        int row = 0, col = 0;
        
        for (int i = 0; i < tblSchedule.getColumnCount(); i++) {
            // Get header name at current index
            String headerName = tblSchedule.getColumnName(i);
            if (headerName.equals(day)){
                col = i;
                break;
            }
        }
            
        for (int i = 0; i < tblSchedule.getRowCount(); i++) {
            // Get header name at current index
            String rowdata = tblSchedule.getValueAt(i, 0).toString();
            if (rowdata.equals(time)){
                row = i;
                break;
            }
        }
        
        Color color = getTableCellBackground.getTableCellBackground(tblSchedule, row, col);
        
        if(!color.equals(tblSchedule.getBackground())){
            JOptionPane.showMessageDialog(this, "Class timing is clashing with other class. Please edit selection.", 
                                                "Information", 
                                                JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            try {
                Class cl = new Class(moduleID);
                int check = cl.saveNewClass(classType, ID, name, lecturer, day, startTime, duration, sizelimit, venue);

                if (check == 1){
                    // message box pop up
                    JOptionPane.showMessageDialog(this, "Class data has been recorded", 
                                                    "Information", 
                                                    JOptionPane.INFORMATION_MESSAGE);
                    resetForm();
                    new Admin_8AssignClass().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Class data isn't recorded. Try again.", 
                                                    "Information", 
                                                    JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                // Code to handle the exception if it occurs// message box pop up
                JOptionPane.showMessageDialog(this, "Student data couldn't be recorded. Try again.", 
                                                "Information", 
                                                JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

