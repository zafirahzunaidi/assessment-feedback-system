    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String ID = lblID.getText().substring(lblID.getText().indexOf(' ') +1);
        String username = txtUsername.getText();
        String name = txtFullName.getText();
        String email = txtEmail.getText().toLowerCase();
        String phoneNo = txtPhone.getText();
        String course = (String) cmbCourse.getSelectedItem();

        Student student = new Student();
        int check = student.registerStudent(ID, username, name, email, phoneNo, course);

        if (check == 1){
            // message box pop up
            JOptionPane.showMessageDialog(this, "Student data has been recorded", 
                                            "Information", 
                                            JOptionPane.INFORMATION_MESSAGE);
            //pergi page 2 balik
            AdminTask task = new AdminTask();
            List<String> data = task.readtxt("student.txt");
            task.addRowtoTable(tableStudent, data, 1);
            
            new Admin_2StudentMain().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Student data isn't recorded. Try again.", 
                                            "Information", 
                                            JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

