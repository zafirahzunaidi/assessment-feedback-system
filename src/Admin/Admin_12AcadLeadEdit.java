    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String username = txtUsername.getText();
        String ID = lblID.getText().substring(lblID.getText().indexOf(' ') +1);
        String name = txtFullName.getText();
        String facultyIC = (String) cmbFaculty.getSelectedItem();
        
        AcademicLeader acadlead = new AcademicLeader();
        int check = acadlead.registerAcadlead(ID, username, name, facultyIC, " ", " ", " ");

        if (check == 1){
            // message box pop up
            JOptionPane.showMessageDialog(this, "Academic leader data has been recorded",
                "Information",
                JOptionPane.INFORMATION_MESSAGE);
            //pergi page 2 balik
            AdminTask task = new AdminTask();
            List<String> data = AdminTask.readtxt("academicLeader.txt");
            task.addRowtoTable(tblAcadlead, data, 8);

            new Admin_10AcadLeadMain().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Academic leader data isn't recorded. Try again.",
                "Information",
                JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

