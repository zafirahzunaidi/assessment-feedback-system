    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int check = 0;
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        
        String oldusername = Session.getUsername();
        String oldpassword = Session.getPassword();
        
        //System.out.println(username.equals(oldusername) && password.equals(oldpassword));
        
        if (username.equals(oldusername) && password.equals(oldpassword)){
            JOptionPane.showMessageDialog(this, "No changes.",
                "Information",
                JOptionPane.INFORMATION_MESSAGE);
            check = 1;
            return;
        } 
        
        if (!password.equals(oldpassword)){
            if (password.length() < 8){
                JOptionPane.showMessageDialog(this, "Password is too short.",
                    "Information",
                    JOptionPane.INFORMATION_MESSAGE);
                check = 1;
                return;
        }
            
        if (!username.equals(oldusername)) {
            boolean exist = User.checkusernameExist(oldusername, username);
            if (exist == true){
                JOptionPane.showMessageDialog(this, "Username exist. Try again.",
                    "Information",
                    JOptionPane.INFORMATION_MESSAGE);
                check = 1;
                return;
            }
        }
        
        if (check == 0) {
            User.editAdmin(oldusername, username, password);
            Session.setPassword(password);
            Session.setUsername(username);
            JOptionPane.showMessageDialog(this, "Edited information has been saved.",
                    "Information",
                    JOptionPane.INFORMATION_MESSAGE);
            } 
            new Admin_13Settings().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

