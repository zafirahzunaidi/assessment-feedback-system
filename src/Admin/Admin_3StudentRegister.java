    public void resetForm() {
        cmbCourse.removeAllItems();
        txtFullName.setText("");
        txtEmail.setText("");
        txtPhone.setText("");
        cmbCourse.setSelectedIndex(-1);
        String newID = "";
        Student student = new Student();
        String lastID = student.getlastIDnumber();
        int numberID = Integer.parseInt(lastID.substring(2));
        numberID += 1;
        if(String.valueOf(numberID).length() != 6){
            newID = String.format("%" + 6 + "s", String.valueOf(numberID)).replace(' ', '0');
        }
        newID = "TP" + newID;
        
        txtUsername.setText(newID.toLowerCase());
        txtPw.setText("12345678");
        txtID.setText(newID);
        
        //addcourse in combo box
        AdminTask task = new AdminTask();
        task.addCoursetoCmb(cmbCourse);
    }

