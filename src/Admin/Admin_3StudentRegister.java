    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String username = txtUsername.getText();       
        String password = txtPw.getText();
        String ID = txtID.getText();
        String name = txtFullName.getText();
        String email = txtEmail.getText().toLowerCase();
        String phoneNo = txtPhone.getText();
        String course = (String) cmbCourse.getSelectedItem();
        
        try {
            Student student = new Student();
            int check = student.registerStudent(ID, username, name, email, phoneNo, course);
            
            User user = new User();
            user.createUser(username, password, "Student");
            
            if (check == 1){
                // message box pop up
                JOptionPane.showMessageDialog(this, "Student data has been recorded", 
                                                "Information", 
                                                JOptionPane.INFORMATION_MESSAGE);
                resetForm();
                List<String> data = AdminTask.readtxt("student.txt");
                AdminTask.addRowtoTable(tableStudent, data, 1);
            } else {
                JOptionPane.showMessageDialog(this, "Student data isn't recorded. Try again.", 
                                                "Information", 
                                                JOptionPane.INFORMATION_MESSAGE);
            }
            
        } catch (Exception e) {
            // Code to handle the exception if it occurs// message box pop up
            JOptionPane.showMessageDialog(this, "Student data couldn't be recorded. Try again.", 
                                            "Information", 
                                            JOptionPane.INFORMATION_MESSAGE);
            
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

