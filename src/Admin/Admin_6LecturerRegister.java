    private void resetForm() {
        //cmbTitle.removeAllItems();
        cmbacadlead.removeAllItems();
        txtFullName.setText("");
        txtEmail.setText("");
        txtPhone.setText("");
        txtacadQual.setText("");
        cmbTitle.setSelectedIndex(-1);
        cmbacadlead.setSelectedIndex(-1);
        String newID = "";
        Lecturer lect = new Lecturer();
        String lastID = lect.getlastIDnumber();
        int numberID = Integer.parseInt(lastID.substring(2));
        numberID += 1;
        if(String.valueOf(numberID).length() != 4){
            newID = String.format("%" + 4 + "s", String.valueOf(numberID)).replace(' ', '0');
        }
        newID = "L" + newID;
        
        txtUsername.setText(newID.toLowerCase());
        txtPw.setText("12345678");
        txtID.setText(newID);
        
        //addcourse in combo box
        AcademicLeader acadlead = new AcademicLeader();
        acadlead.addAcadleadtoCmb(cmbacadlead);
    }
    
