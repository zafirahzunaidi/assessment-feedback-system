    private static class AssessmentRow {
        String assessId, classId, assessType;
        double fullMark;
        double weightage;
        AssessmentRow(String assessId, String classId, String assessType, double fullMark, double weightage) {
            this.assessId = assessId;
            this.classId = classId;
            this.assessType = assessType;
            this.fullMark = fullMark;
            this.weightage = weightage;
        }
    }
    
    private static class ResultRow {
        String resultId, studentId, assessId;
        Double markReceived;   // null = not released
        String feedback;
        ResultRow(String resultId, String studentId, String assessId, Double markReceived, String feedback) {
            this.resultId = resultId;
            this.studentId = studentId;
            this.assessId = assessId;
            this.markReceived = markReceived;
            this.feedback = feedback;
        }
    }
    
    private void initCustom() {
        cbClass.removeAllItems();
        cbClass.addItem("-- Select Class --");

        cbAssessment.removeAllItems();
        cbAssessment.addItem("-- Select Assessment --");

        clearDisplay();

        // load data from files
        loadClasses();
        loadRegisteredClassesForCurrentStudent();
        loadAssessments();
        loadResults();

        // only choose registered classes
        populateClassCombo();
    }

    private void loadClasses() {
        classById.clear();
        List<ClassInfo> classes = new ClassData().readAll();
        for (ClassInfo c : classes) {
            classById.put(c.getClassID(), c);
        }
    }
    
    private void loadRegisteredClassesForCurrentStudent() {
        registeredClassIds.clear();

        String studentId = Session.getStudentId();
        if (studentId == null || studentId.isBlank()) return;

        List<RegisteredClass> regs = new RegisteredClassData().findByStudentId(studentId);
        for (RegisteredClass rc : regs) {
            registeredClassIds.add(rc.getClassID());
        }

        Collections.sort(registeredClassIds);
    }
    
    private void loadAssessments() {
        assessmentsByClass.clear();

        AssessmentData ad = new AssessmentData();

        for (String classId : registeredClassIds) {
            List<Assessment> list = ad.findByClassId(classId);

            for (Assessment a : list) {
                AssessmentRow row = new AssessmentRow(
                    a.getAssessID(),
                    a.getClassID(),
                    a.getAssessType(),
                    (double) a.getFullMark(),
                    a.getWeightage()
                );

                assessmentsByClass
                    .computeIfAbsent(classId, k -> new ArrayList<>())
                    .add(row);
            }
        }

        for (List<AssessmentRow> list : assessmentsByClass.values()) {
            list.sort(Comparator.comparing(x -> x.assessId));
        }
    }
    
    private void loadResults() {
        resultByStudentAssess.clear();

        String studentId = Session.getStudentId();
        if (studentId == null || studentId.isBlank()) return;

        ResultData rd = new ResultData();
        List<Result> results = rd.findByStudentId(studentId);

        for (Result r : results) {
            ResultRow rr = new ResultRow(
                r.getResultID(),
                r.getStudentID(),
                r.getAssessID(),
                r.getMarkReceived(),
                r.getFeedback()
            );

            resultByStudentAssess.put(key(r.getStudentID(), r.getAssessID()), rr);
        }
    }
    
    private void populateClassCombo() {
        // keep first item
        for (String classId : registeredClassIds) {
            ClassInfo c = classById.get(classId);
            String display = (c == null) ? classId : (classId + " - " + c.getName());
            cbClass.addItem(display);
        }

        if (registeredClassIds.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "No registered classes found.\nPlease register a class first.");
        }
    }
    
