    private void onClassChanged() {
        clearDisplay();

        String classId = getSelectedClassId();
        if (classId == null) {
            cbAssessment.removeAllItems();
            cbAssessment.addItem("-- Select Assessment --");
            return;
        }

        lblAssDetailClassResult.setText(classId);

        cbAssessment.setEnabled(true);
        cbAssessment.removeAllItems();
        cbAssessment.addItem("-- Select Assessment --");

        List<AssessmentRow> list = assessmentsByClass.getOrDefault(classId, Collections.emptyList());
        for (AssessmentRow a : list) {
            cbAssessment.addItem(a.assessId + " - " + a.assessType);
        }

        if (list.isEmpty()) {
            taLectAssFeedback.setText("No assessments found for this class.");
        }
    }
    
    private void onAssessmentChanged() {
        String classId = getSelectedClassId();
        String assessId = getSelectedAssessId();

        if (classId == null || assessId == null) return;

        // find assessment row
        AssessmentRow chosen = null;
        for (AssessmentRow a : assessmentsByClass.getOrDefault(classId, Collections.emptyList())) {
            if (a.assessId.equalsIgnoreCase(assessId)) {
                chosen = a;
                break;
            }
        }
        if (chosen == null) return;

        // details panel
        lblAssDetailClassResult.setText(chosen.classId);
        lblAssDetailAssTypeResult.setText(chosen.assessType);
        lblAssDetailFullMarkResult.setText(String.valueOf(chosen.fullMark));
        lblAssDetailWeightageResult.setText(formatWeightage(chosen.weightage));

        // result & feedback
        String studentId = Session.getStudentId();
        ResultRow rr = (studentId == null) ? null : resultByStudentAssess.get(key(studentId, chosen.assessId));

        if (rr == null || rr.markReceived == null) {
            lblResultsMarkRecResult.setText("— / " + chosen.fullMark);
            lblResultsResStatResult.setText("Pending");
            taLectAssFeedback.setText("No feedback available.");
        } else {
            lblResultsMarkRecResult.setText(fmtMark(rr.markReceived) + " / " + chosen.fullMark);
            lblResultsResStatResult.setText("Released");
            taLectAssFeedback.setText(rr.feedback == null || rr.feedback.isBlank() ? "No feedback available." : rr.feedback);
        }
    }
    
    private static String fmtMark(Double v) {
        if (v == null) return "—";
        if (Math.abs(v - Math.round(v)) < 0.0000001) return String.valueOf((long)Math.round(v));
        return String.valueOf(v);
    }
    
