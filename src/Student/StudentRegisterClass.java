    private void btnRegisterSelectedClassesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterSelectedClassesActionPerformed
        String studentId = Session.getStudentId();
        if (studentId == null || studentId.isBlank()) {
            JOptionPane.showMessageDialog(this,
                    "Session student not set.\nSet Session.setStudentId(...) before using this page.",
                    "Session Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        studentId = studentId.trim();

        // get selected classes (2 rows)
        List<ClassInfo> selectedClasses = getSelectedClassesFromTable();

        // validate rule (1 lecture + 1 lab/tutorial, same module)
        if (!validateTwoClassSelection(selectedClasses)) {
            return;
        }

        RegisteredClassData rcData = new RegisteredClassData();
        ClassData classData = new ClassData();

        // confirm
        String classA = selectedClasses.get(0).getClassID();
        String classB = selectedClasses.get(1).getClassID();

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Register these classes?\n" + classA + " and " + classB,
                "Confirm Registration",
                JOptionPane.YES_NO_OPTION
        );
        if (confirm != JOptionPane.YES_OPTION) return;

        // capacity + duplicate checks for BOTH
        for (ClassInfo cls : selectedClasses) {
            String classId = cls.getClassID();

            if (rcData.exists(studentId, classId)) {
                JOptionPane.showMessageDialog(this,
                        "You already registered class: " + classId,
                        "Duplicate", JOptionPane.WARNING_MESSAGE);
                return;
            }

            ClassInfo fullInfo = classData.findById(classId);
            if (fullInfo == null) {
                JOptionPane.showMessageDialog(this,
                        "Class not found in class.txt: " + classId,
                        "Data Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int used = rcData.countByClassId(classId);
            int limit = fullInfo.getSizeLimit();
            if (used >= limit) {
                JOptionPane.showMessageDialog(this,
                        "Class is full: " + classId + " (" + used + "/" + limit + ")",
                        "Full", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        // append both registrations
        String date = LocalDate.now().format(DateTimeFormatter.ISO_DATE);

        for (ClassInfo cls : selectedClasses) {
            String rcId = generateNextRegisteredClassId(); // generates new ID each time
            RegisteredClass newRc = new RegisteredClass(rcId, cls.getClassID(), studentId, date);

            boolean ok = rcData.append(newRc);
            if (!ok) {
                JOptionPane.showMessageDialog(this,
                        "Failed to register class: " + cls.getClassID(),
                        "Write Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        JOptionPane.showMessageDialog(this, "Registered successfully (Lecture + Lab/Tutorial).");

        loadDataFromFiles();
        applyFilters();
        
        // create moduleResult row if not exist
        String moduleId = getModuleIdByClassId(selectedClasses.get(0).getClassID());

        if (moduleId != null && !moduleId.isBlank()) {
            ModuleResultData mrd = new ModuleResultData();

            if (!mrd.existsStudentModule(studentId, moduleId)) {
                String mrId = mrd.generateNextModuleResultId();

                ModuleResult newMr = new ModuleResult(mrId, studentId, moduleId, 0.0, 0.0, 0.0, null);

                boolean okMr = mrd.append(newMr);
                if (!okMr) {
                    JOptionPane.showMessageDialog(this,
                            "Registered classes, but failed to create ModuleResult row.\nPlease check moduleResult.txt path.",
                            "ModuleResult Warning", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnRegisterSelectedClassesActionPerformed

