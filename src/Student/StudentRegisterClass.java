    private void loadRequiredModulesForCurrentStudent() {
        requiredModuleIds.clear();

        String studentId = Session.getStudentId();
        if (studentId == null || studentId.isBlank()) {
            return;
        }

        StudentData sData = new StudentData();
        Student s = sData.findById(studentId.trim());

        if (s == null || s.getCurrentCourse() == null || s.getCurrentCourse().trim().isEmpty()) {
            return;
        }

        String courseId = s.getCurrentCourse().trim();

        for (String[] r : FilePath.readAll(FilePath.REQUIRED_MODULE_FILE)) {
            if (r.length < 2) continue;
            if (r[0].equalsIgnoreCase("courseID")) continue;

            String cId = r[0].trim();
            String mId = r[1].trim();

            if (cId.equalsIgnoreCase(courseId)) {
                requiredModuleIds.add(mId);
            }
        }
    }

    private void loadDataFromFiles() {
        allRows.clear();
        moduleLectToModule.clear();
        registeredCountByClass.clear();
        registeredStudentsByClass.clear();

        for (String[] r : FilePath.readAll(FilePath.MODULE_LECTURER_FILE)) {
            if (r.length < 3) continue;
            if (r[0].equalsIgnoreCase("modlectID") || r[0].equalsIgnoreCase("moduleLectID")) continue;
            moduleLectToModule.put(r[0].trim(), r[2].trim());
        }

        RegisteredClassData rcData = new RegisteredClassData();
        for (RegisteredClass rc : rcData.readAll()) {
            String classId = rc.getClassID();
            String studentId = rc.getStudentID();

            registeredCountByClass.put(classId, registeredCountByClass.getOrDefault(classId, 0) + 1);

            registeredStudentsByClass
                    .computeIfAbsent(classId, k -> new HashSet<>())
                    .add(studentId);
        }

        ClassData classData = new ClassData();
        List<ClassInfo> classes = classData.readAll();

        String currentStudentId = Session.getStudentId();

        for (ClassInfo c : classes) {
            String classId = c.getClassID();
            String name = c.getName();
            String type = c.getType();
            String day = c.getClassDay();
            String timeSlot = formatTimeSlot(c.getStartTime(), c.getDurationHour());
            String venue = c.getVenue();

            int limit = c.getSizeLimit();
            int used = registeredCountByClass.getOrDefault(classId, 0);

            String capacity = used + "/" + limit;

            // status priority: Registered > Full > Open
            boolean isRegistered = currentStudentId != null
                    && registeredStudentsByClass.getOrDefault(classId, Collections.emptySet())
                    .contains(currentStudentId);

            String status;
            if (isRegistered) status = "Registered";
            else if (used >= limit) status = "Full";
            else status = "Open";

            String moduleId = moduleLectToModule.getOrDefault(c.getModuleLectID(), "");
            
            if (!requiredModuleIds.isEmpty() && !requiredModuleIds.contains(moduleId)) {
                continue;
            }
            
            allRows.add(new String[]{classId, name, type, day, timeSlot, venue, capacity, status, moduleId});
        }
    }

    private void applyFilters() {
        String moduleSel = (String) cbModule.getSelectedItem();
        String typeSel = (String) cbType.getSelectedItem();

        boolean moduleActive = moduleSel != null && !moduleSel.startsWith("--");
        boolean typeActive = typeSel != null && !typeSel.startsWith("--");

        String selectedModuleId = "";
        if (moduleActive) {
            // "MATHS - Mathematics" -> "MATHS"
            selectedModuleId = moduleSel.split(" - ")[0].trim();
        }
        
        tableModel.setRowCount(0);

        for (String[] r : allRows) {
            String rowType = r[2];
            String rowModuleId = r[8];

            if (typeActive && !rowType.equalsIgnoreCase(typeSel)) continue;
            if (moduleActive && !rowModuleId.equalsIgnoreCase(selectedModuleId)) continue;
            if ("Registered".equalsIgnoreCase(r[7])) continue;
            // add only visible columns to table
            tableModel.addRow(new Object[]{r[0], r[1], r[2], r[3], r[4], r[5], r[6], r[7]});
        }

        if (tblAvailableClasses.getSelectedRowCount() > 0) {
            tblAvailableClasses.clearSelection();
        }
    }
    
    private String formatTimeSlot(String startTimeHHmm, double durationHour) {
        try {
            DateTimeFormatter inFmt = DateTimeFormatter.ofPattern("HHmm");
            DateTimeFormatter outFmt = DateTimeFormatter.ofPattern("HH.mm");

            LocalTime start = LocalTime.parse(startTimeHHmm, inFmt);
            int minutes = (int) Math.round(durationHour * 60.0);
            LocalTime end = start.plusMinutes(minutes);

            return start.format(outFmt) + " - " + end.format(outFmt);
        } catch (Exception e) {
            // fallback if data is weird
            return startTimeHHmm;
        }
    }
    
    private String generateNextRegisteredClassId() {
        RegisteredClassData rcData = new RegisteredClassData();
        int max = 0;

        for (RegisteredClass rc : rcData.readAll()) {
            String id = rc.getRegisteredClassID(); // e.g. RC00001
            if (id == null) continue;

            id = id.trim().toUpperCase();
            if (!id.startsWith("RC")) continue;

            // extract digits
            String numStr = id.substring(2).replaceAll("[^0-9]", "");
            if (numStr.isEmpty()) continue;

            try {
                int n = Integer.parseInt(numStr);
                if (n > max) max = n;
            } catch (NumberFormatException ignored) {}
        }

        int next = max + 1;
        return String.format("RC%05d", next);
    }
    
    private List<ClassInfo> getSelectedClassesFromTable() {
        int[] rows = tblAvailableClasses.getSelectedRows();
        if (rows == null || rows.length == 0) return Collections.emptyList();

        List<ClassInfo> selected = new ArrayList<>();
        ClassData cd = new ClassData();

        for (int r : rows) {
            String classId = tblAvailableClasses.getValueAt(r, 0).toString().trim();
            ClassInfo c = cd.findById(classId);
            if (c != null) selected.add(c);
        }
        return selected;
    }
    
    private String classTypeBucket(String type) {
        if (type == null) return "";
        type = type.trim().toLowerCase();

        if (type.contains("lecture")) return "LECTURE";
        if (type.contains("lab")) return "LAB_TUTORIAL";
        if (type.contains("tutorial")) return "LAB_TUTORIAL";

        return "OTHER";
    }
    
    private String getModuleIdByClassId(String classId) {
        for (String[] r : allRows) {
            if (r.length >= 9 && r[0].equalsIgnoreCase(classId)) {
                return r[8].trim(); // moduleId
            }
        }
        return "";
    }
    
    private boolean validateTwoClassSelection(List<ClassInfo> selected) {

        if (selected == null || selected.size() != 2) {
            JOptionPane.showMessageDialog(this,
                    "You must choose exactly TWO classes:\n"
                  + "✔ 1 Lecture class\n"
                  + "✔ 1 Lab OR Tutorial class",
                    "Selection required",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }

        ClassInfo c1 = selected.get(0);
        ClassInfo c2 = selected.get(1);

        String moduleA = getModuleIdByClassId(c1.getClassID());
        String moduleB = getModuleIdByClassId(c2.getClassID());

        if (moduleA.isEmpty() || moduleB.isEmpty() || !moduleA.equalsIgnoreCase(moduleB)) {
            JOptionPane.showMessageDialog(this,
                    "Both selected classes must be from the SAME module.",
                    "Invalid selection",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }

        String bucket1 = classTypeBucket(c1.getType());
        String bucket2 = classTypeBucket(c2.getType());

        boolean hasLecture = bucket1.equals("LECTURE") || bucket2.equals("LECTURE");
        boolean hasLabTut = bucket1.equals("LAB_TUTORIAL") || bucket2.equals("LAB_TUTORIAL");

        if (!hasLecture || !hasLabTut) {
            JOptionPane.showMessageDialog(this,
                    "Invalid combination.\n\n"
                  + "You must select:\n"
                  + "✅ ONE Lecture\n"
                  + "✅ ONE Lab OR Tutorial (only one of these)",
                    "Invalid selection",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }

        if (bucket1.equals(bucket2)) {
            JOptionPane.showMessageDialog(this,
                    "You selected 2 classes from the same category.\n"
                  + "Please select ONE Lecture + ONE Lab/Tutorial.",
                    "Invalid selection",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
    }
    
