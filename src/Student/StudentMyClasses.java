    private static class ClassCardPanel extends JPanel {
        private final JLabel classBanner = new JLabel();
        private final JLabel txtClassName = new JLabel();
        private final JLabel txtClassType = new JLabel();
        private final JButton btnOpen = new JButton(">");

        ClassCardPanel(String moduleName, String classType, java.awt.event.ActionListener openListener) {
            buildUI();
            setModuleName(moduleName);
            setClassType(classType);
            setOpenListener(openListener);
        }

        private void buildUI() {
            setPreferredSize(new Dimension(240, 180));
            setBackground(Color.WHITE);
            setLayout(new BorderLayout());
            setBorder(BorderFactory.createLineBorder(new Color(210, 210, 210), 1, true));

            classBanner.setOpaque(true);
            classBanner.setBackground(new Color(210, 235, 250));
            classBanner.setPreferredSize(new Dimension(240, 95));
            classBanner.setHorizontalAlignment(SwingConstants.CENTER);
            classBanner.setText(" ");
            add(classBanner, BorderLayout.NORTH);

            JPanel pnlBottom = new JPanel(new BorderLayout());
            pnlBottom.setOpaque(false);
            pnlBottom.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
            add(pnlBottom, BorderLayout.CENTER);

            JPanel pnlText = new JPanel();
            pnlText.setOpaque(false);
            pnlText.setLayout(new BoxLayout(pnlText, BoxLayout.Y_AXIS));

            txtClassName.setFont(new Font("Arial", Font.PLAIN, 12));
            txtClassType.setFont(new Font("Arial", Font.PLAIN, 10));

            pnlText.add(txtClassName);
            pnlText.add(Box.createVerticalStrut(4));
            pnlText.add(txtClassType);

            pnlBottom.add(pnlText, BorderLayout.CENTER);

            btnOpen.setText(">");
            btnOpen.setFont(new Font("Arial", Font.BOLD, 20));
            btnOpen.setForeground(new Color(60, 60, 60));
            btnOpen.setContentAreaFilled(false);
            btnOpen.setBorderPainted(false);
            btnOpen.setOpaque(false);
            btnOpen.setPreferredSize(new Dimension(42, 28));
            
            pnlBottom.add(btnOpen, BorderLayout.EAST);
        }

        void setModuleName(String moduleName) {
            txtClassName.setText("<html><body style='width:160px'>"
                    + escapeHtml(moduleName) + "</body></html>");
        }

        void setClassType(String classType) {
            txtClassType.setText(classType == null ? "" : classType);
        }

        void setOpenListener(java.awt.event.ActionListener l) {
            for (var old : btnOpen.getActionListeners()) btnOpen.removeActionListener(old);
            if (l != null) btnOpen.addActionListener(l);
        }

        private static String escapeHtml(String s) {
            if (s == null) return "";
            return s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;");
        }
    }
    
    private JPanel createCard(String moduleName, String classType, java.awt.event.ActionListener openListener) {
        return new ClassCardPanel(moduleName, classType, openListener);
    }
    
