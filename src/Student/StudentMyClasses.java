    private void renderRegisteredClassCards() {
        pnlClassCards.removeAll();

        // session check
        if (!Session.isLoggedIn() || Session.getStudentId() == null || Session.getStudentId().trim().isEmpty()) {
            pnlClassCards.add(createCard("No session student", "",
                    e -> JOptionPane.showMessageDialog(this,
                            "Session student not set.\nSet Session.setStudentId(...) in Main().")));
            refreshCardsPanel();
            return;
        }

        String studentId = Session.getStudentId().trim();

        // read registered classes for this student
        RegisteredClassData rcData = new RegisteredClassData();
        List<RegisteredClass> regs = rcData.findByStudentId(studentId);

        if (regs == null || regs.isEmpty()) {
            pnlClassCards.add(createCard("No registered classes", "",
                    e -> JOptionPane.showMessageDialog(this,
                            "This student has no registered classes.\nClick 'Register New Class' to register.")));
            refreshCardsPanel();
            return;
        }

        // lookup class details
        ClassData classData = new ClassData();

        for (RegisteredClass rc : regs) {
            if (rc == null || rc.getClassID() == null) continue;

            String classId = rc.getClassID().trim();
            ClassInfo ci = classData.findById(classId);

            if (ci == null) {
                pnlClassCards.add(createCard(classId, "Unknown Class",
                        e -> JOptionPane.showMessageDialog(this,
                                "Class not found in class.txt: " + classId,
                                "Data Error",
                                JOptionPane.ERROR_MESSAGE)));
                continue;
            }

            JPanel card = createCard(ci.getName(), ci.getType(), e -> {
                // store selected class in session for other screens
                Session.setSelectedClassId(ci.getClassID());

                new StudentClassSchedule().setVisible(true);
                dispose();
            });

            pnlClassCards.add(card);
        }

        refreshCardsPanel();
    }
    
    private void refreshCardsPanel() {
        pnlClassCards.revalidate();
        pnlClassCards.repaint();
    }

