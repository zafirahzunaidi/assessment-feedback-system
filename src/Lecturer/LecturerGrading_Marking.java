    private void SubmitMarksButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitMarksButtonActionPerformed
        if (AssessmentMarkingListTable.isEditing()) {
            AssessmentMarkingListTable.getCellEditor().stopCellEditing();
        }

        DefaultTableModel model = (DefaultTableModel) AssessmentMarkingListTable.getModel();

        Assessment a = lecturer.getAssessmentByID(assessID);

        // Validation
        boolean hasAnyMark = false;

        for (int row = 0; row < model.getRowCount(); row++) {
            Object val = model.getValueAt(row, 2);
            
            if (val == null || val.toString().trim().isEmpty()) {
                continue; // allow empty
            }

            hasAnyMark = true;

            try {
                double mark = Double.parseDouble(val.toString());

                if (!a.isValidMark(mark)) {
                    throw new NumberFormatException();
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(
                    this,
                    "Invalid mark detected.\n"
                  + "Please enter a number between 0 and "
                  + a.getFullMark(),
                    "Invalid Input",
                    JOptionPane.ERROR_MESSAGE
                );
                return;
            }
        }

        if (!hasAnyMark) {
            JOptionPane.showMessageDialog(
                this,
                "No marks entered.",
                "Nothing to Submit",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        // Save result & update module result
        for (int row = 0; row < model.getRowCount(); row++) {

            String studentID = model.getValueAt(row, 0).toString();

            Object markObj = model.getValueAt(row, 2);
            Double mark = (markObj == null || markObj.toString().isEmpty())
                ? null
                : Double.parseDouble(markObj.toString());

            String feedback =
                model.getValueAt(row, 4) == null
                ? ""
                : model.getValueAt(row, 4).toString();

            lecturer.saveResult(studentID, assessID, mark, feedback);

            if (mark != null) {
                String moduleID = lecturer.getModuleIDFromClass(a.getClassID());
                lecturer.updateModuleResult(studentID, moduleID);
            }

        }

        JOptionPane.showMessageDialog(
            this,
            "Marks submitted successfully.",
            "Success",
            JOptionPane.INFORMATION_MESSAGE
        );

        new LecturerGrading(lecturer).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_SubmitMarksButtonActionPerformed

