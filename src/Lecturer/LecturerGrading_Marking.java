    private void setupTable() {
        DefaultTableModel model = new DefaultTableModel(
            new Object[]{"Student ID", "Name", "Mark", "Weightage %", "Feedback"},
            0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {

                return column == 2 || column == 4;
            }
        };

        AssessmentMarkingListTable.setModel(model);
        AssessmentMarkingListTable.setRowHeight(36);
        AssessmentMarkingListTable.setShowGrid(true);
        AssessmentMarkingListTable.setGridColor(new Color(230,230,230));
        AssessmentMarkingListTable.getTableHeader().setReorderingAllowed(false);

        model.addTableModelListener(e -> {
        if (e.getColumn() != 2) return;

        int row = e.getFirstRow();
        Object value = model.getValueAt(row, 2);

        if (value == null || value.toString().isEmpty()) {
            model.setValueAt("", row, 3);
            return;
        }

        try {
            double mark = Double.parseDouble(value.toString());
            Assessment a = lecturer.getAssessmentByID(assessID);

            if (!a.isValidMark(mark)) {
                JOptionPane.showMessageDialog(
                    this,
                    "Mark must be between 0 and " + a.getFullMark(),
                    "Invalid Mark",
                    JOptionPane.ERROR_MESSAGE
                );
                model.setValueAt("", row, 2);
                model.setValueAt("", row, 3);
                return;
            }

            double weighted = a.calculateWeightedMark(mark);
            model.setValueAt(String.format("%.2f", weighted), row, 3);

        } catch (NumberFormatException ex) {
            model.setValueAt("", row, 3);
        }
        });
    }

    private void loadStudentMarkingList() {
        // Get assessment
        Assessment a = lecturer.getAssessmentByID(assessID);
        if (a == null)
            return;

        // Header info
        H2ClassName.setText(a.getClassID());
        H2AssessmentType.setText(a.getAssessType());
        H2FullMark_num.setText(String.valueOf(a.getFullMark()));
        H2Weightage_num.setText(String.valueOf(a.getWeightage()));

        // Load students
        DefaultTableModel model = (DefaultTableModel) AssessmentMarkingListTable.getModel();
        model.setRowCount(0);

        for (Student s : lecturer.getStudentsForAssessment(assessID)) {
            if (s == null) 
                continue;

            Result r = lecturer.getResult(s.getStudentID(), assessID);

            Double mark = (r == null) ? null : r.getMarkReceived();
            String feedback = "";

            if (r != null && r.getFeedback() != null 
                    && !r.getFeedback().equalsIgnoreCase("NULL")) {
                feedback = r.getFeedback();
            }

            Double weighted = (mark == null) ? null : a.calculateWeightedMark(mark);

            model.addRow(new Object[]{
                s.getStudentID(),
                s.getName(),
                mark,
                weighted == null ? "" : String.format("%.2f", weighted),
                feedback
            });
        }
    }
    

