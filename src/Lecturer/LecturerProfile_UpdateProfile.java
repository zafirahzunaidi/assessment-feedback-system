    private void ConfirmUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmUpdateButtonActionPerformed
        ProfileUpdateResult result = lecturer.updateProfile(
            UsernameTextField.getText().trim(),
            NameTextField.getText().trim(),
            TitleComboBox.getSelectedItem().toString().isBlank() ? null
                : TitleComboBox.getSelectedItem().toString(),
            EmailTextField.getText().trim(),
            PhoneNumberTextField.getText().trim(),
            AcadQualTextField.getText().trim(),
            this
        );

        switch (result) {
            case SUCCESS -> {
                JOptionPane.showMessageDialog(this, "Profile updated successfully.");
                new LecturerProfile(lecturer).setVisible(true);
                dispose();
            }
            case EMPTY_FIELDS -> showError("All fields must be filled.");
            case INVALID_EMAIL -> showError("Invalid email address.");
            case INVALID_PHONE -> showError("Invalid phone number.");
            case USERNAME_EXISTS -> showError("Username already exists.");
            case PASSWORD_FAILED -> showError("Update cancelled due to failed authentication.");
            case IO_ERROR -> showError("Failed to save data.");
        }
        
        new LecturerProfile(lecturer).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_ConfirmUpdateButtonActionPerformed

