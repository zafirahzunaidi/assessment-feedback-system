    private void loadProfileData() {
        if (lecturer == null)
            return;

        LectIDTextField.setText(lecturer.getLecturerID());
        NameTextField.setText(lecturer.getName());
        EmailTextField.setText(lecturer.getEmail());
        PhoneNumberTextField.setText(lecturer.getPhoneNumber());
        AcadQualTextField.setText(lecturer.getAcademicQualification());
        AcadLeaderTextField.setText(lecturer.getAcadLeadID());
        UsernameTextField.setText(lecturer.getUsername());

        String title = lecturer.getTitle();
        if (title == null || title.equalsIgnoreCase("NULL")) {
            TitleComboBox.setSelectedIndex(0); // empty option
        } else {
            TitleComboBox.setSelectedItem(title);
        }
        
        AcadLeaderTextField.setText(
            lecturer.getAcademicLeaderName()
        );
    }
    
    
    // Read only. User not allowed to edit certain field.
    private void setReadOnly() {
        JTextField[] fields = {
            LectIDTextField, NameTextField, AcadLeaderTextField
        };

        for (JTextField f : fields) {
            f.setEditable(false);
            f.setBackground(new Color(252, 252, 252));
        }
    }

