    private void ConfirmDesignButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmDesignButtonActionPerformed
        String classID = (String) ClassComboBox.getSelectedItem();
        String assessType = (String) AssessmentTypeComboBox.getSelectedItem();

        if (classID == null || assessType == null
            || FullMarkTextField.getText().isEmpty()
            || WeightageTextField.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this,
                "Please fill in all fields.",
                "Validation Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        double fullMark, weightage;

        try {
            fullMark = Double.parseDouble(FullMarkTextField.getText());
            weightage = Double.parseDouble(WeightageTextField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                "Full mark and weightage must be numbers.",
                "Input Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (fullMark <= 0 || fullMark > 100) {
            JOptionPane.showMessageDialog(this,
                "Full mark must be between 1 and 100.",
                "Invalid Full Mark",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (weightage <= 0) {
            JOptionPane.showMessageDialog(this,
                "Weightage must be greater than 0.",
                "Invalid Weightage",
                JOptionPane.ERROR_MESSAGE);
            return;
        }


        AssessmentCreateResult result = lecturer.createAssessment(classID, assessType, fullMark, weightage);

        switch (result) {
            case SUCCESS -> {
                JOptionPane.showMessageDialog(this,
                    "Assessment created successfully!",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);
                new LecturerAssessment(lecturer).setVisible(true);
                this.dispose();
            }

            case FINAL_EXAM_EXISTS -> JOptionPane.showMessageDialog(this,
                "This class already has a Final Exam.",
                "Final Exam Exists",
                JOptionPane.ERROR_MESSAGE);

            case WEIGHTAGE_EXCEEDED -> JOptionPane.showMessageDialog(this,
                "Weightage exceeds remaining allocation.",
                "Weightage Error",
                JOptionPane.ERROR_MESSAGE);

            case WEIGHTAGE_FULL -> JOptionPane.showMessageDialog(this,
                "This class already reached 100% weightage.",
                "Weightage Full",
                JOptionPane.ERROR_MESSAGE);

            default -> JOptionPane.showMessageDialog(this,
                "Unable to create assessment.",
                "System Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ConfirmDesignButtonActionPerformed

