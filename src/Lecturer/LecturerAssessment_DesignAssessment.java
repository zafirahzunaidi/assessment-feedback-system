    private void ClassComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ClassComboBoxActionPerformed
        String classID = (String) ClassComboBox.getSelectedItem();
        double used = lecturer.getTotalWeightageForClass(classID);
        double remaining = 1.0 - used;

        CurrentTotalWeightage_num.setText(
            String.format("%.1f / 1.0", used)
        );
        RemainingWeightage_num.setText(
            String.format("%.1f", remaining)
        );

        // Disable confirm if no remaining weightage
        if (remaining <= 0) {
            ConfirmDesignButton.setEnabled(false);
            ConfirmDesignButton.setToolTipText( "This class already reached 100% weightage");
        } else {
            ConfirmDesignButton.setEnabled(true);
            ConfirmDesignButton.setToolTipText(null);
        }
    }//GEN-LAST:event_ClassComboBoxActionPerformed

