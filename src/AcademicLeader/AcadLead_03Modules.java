private void addTableListener() {
    jTable1.addMouseListener(new MouseAdapter() {
        @Override
        public void mouseClicked(MouseEvent e) {
            int selectedRow = jTable1.getSelectedRow();

            if (selectedRow >= 0) {
                jTextField1.setText(jTable1.getValueAt(selectedRow, 0).toString());
                jTextField2.setText(jTable1.getValueAt(selectedRow, 1).toString());
                jTextField3.setText(jTable1.getValueAt(selectedRow, 2).toString());
            }
        }
    });
}

private void addModule() {

    String moduleId = jTextField1.getText().trim();
    String moduleName = jTextField2.getText().trim();
    String creditText = jTextField3.getText().trim();

    // mwssagw for incoorect
    if (moduleId.isEmpty() || moduleName.isEmpty() || creditText.isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Please fill in all fields.",
                "Input Error",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    double creditHours;
    try {
        creditHours = Double.parseDouble(creditText);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
                "Credit hour must be a number (e.g., 3 or 4.0).",
                "Input Error",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    List<Module> modules = Module.loadModules("module.txt");
    for (Module m : modules) {
        if (m.getModuleID().equalsIgnoreCase(moduleId)) {
            JOptionPane.showMessageDialog(this,
                    "Module ID already exists.",
                    "Duplicate Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
    }


    Module newModule = new Module(moduleId, moduleName, creditHours);
    Module.addModule("module.txt", newModule);

    // Refresh
    loadModulesIntoTable();

    // Clear
    clearFields();

    JOptionPane.showMessageDialog(this,
            "Module added successfully!",
            "Success",
            JOptionPane.INFORMATION_MESSAGE);
}

private void clearFields() {
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTable1.clearSelection();
}

private void updateModule() {

    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this,
                "Please select a module to update.",
                "Selection Error",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    String moduleId = jTextField1.getText().trim();
    String moduleName = jTextField2.getText().trim();
    String creditText = jTextField3.getText().trim();

    // fill all
    if (moduleId.isEmpty() || moduleName.isEmpty() || creditText.isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Please fill in all fields.",
                "Input Error",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    double creditHour;
    try {
        creditHour = Double.parseDouble(creditText);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
            "Credit hour must be a number (e.g., 3 or 4.0).",
            "Input Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Load all 
    List<Module> modules = Module.loadModules("module.txt");
    boolean found = false;

    for (Module m : modules) {
        if (m.getModuleID().equalsIgnoreCase(moduleId)) {
            m.setName(moduleName);
            m.setCreditHour(creditHour);
            found = true;
            break;
        }
    }

    if (!found) {
        JOptionPane.showMessageDialog(this,
                "Module ID not found.",
                "Update Error",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Save 
    Module.saveModules("module.txt", modules);

    // Refresh 
    loadModulesIntoTable();

    JOptionPane.showMessageDialog(this,
            "Module updated successfully!",
            "Success",
            JOptionPane.INFORMATION_MESSAGE);
}

private void deleteModule() {

    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this,
                "Please select a module to delete.",
                "Selection Error",
                JOptionPane.ERROR_MESSAGE);
        return;
    }


    int confirm = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to delete this module?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION);

    if (confirm != JOptionPane.YES_OPTION) {
        return; // user cancelled
    }

    String moduleId = jTable1.getValueAt(selectedRow, 0).toString();

    // Load all modules
    List<Module> modules = Module.loadModules("module.txt");

    // Remove module with matching ID
    boolean removed = modules.removeIf(m -> m.getModuleID().equalsIgnoreCase(moduleId));

    if (!removed) {
        JOptionPane.showMessageDialog(this,
                "Module ID not found.",
                "Delete Error",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Save updated list back to file
    Module.saveModules("module.txt", modules);

    // Refresh table and clear fields
    loadModulesIntoTable();
    clearFields();

    JOptionPane.showMessageDialog(this,
            "Module deleted successfully!",
            "Success",
            JOptionPane.INFORMATION_MESSAGE);
}



