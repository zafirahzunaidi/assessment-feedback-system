    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         String newUsername = txtUsername.getText().trim();
        String newName = txtName.getText().trim();
        String newEmail = txtEmail.getText().trim();
        String newPhone = txtPhone.getText().trim();
        String newTitle = txtTitle.getText().trim();
        String newFaculty = txtFaculty.getText().trim();
        String newAcadQual = txtAcadQual.getText().trim();

        // Update Leader object
        currentLeader.setUsername(newUsername);
        currentLeader.setName(newName);
        currentLeader.setEmail(newEmail);
        currentLeader.setPhoneNumber(newPhone);
        currentLeader.setTitle(newTitle);
        currentLeader.setFacultyInCharge(newFaculty);
        currentLeader.setAcademicQualification(newAcadQual);

        // Update User object username if changed
        if (!currentUser.getUsername().equals(newUsername)) {
            currentUser.setUsername(newUsername);
            loggedInUsername = newUsername;
        }

        // Save back to files
        AcademicLeader.saveLeaders("academicLeader.txt", leaders);
        User.saveUsers("user.txt", users);

        populateProfileLabels();

        JOptionPane.showMessageDialog(this, "Profile updated successfully!");
        cardLayout.show(pnlCardsProfile, "pnlProfile");
    }//GEN-LAST:event_jButton4ActionPerformed

