    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String currentPass = P_txtCurrentPassword.getText().trim();
        String newPass = P_txtNewPassword.getText().trim();
        String confirmPass = P_txtConfirmPassword.getText().trim();

        if (!currentUser.getPassword().equals(currentPass)) {
            JOptionPane.showMessageDialog(this, "Current password is incorrect!");
            return;
        }

        if (!newPass.equals(confirmPass)) {
            JOptionPane.showMessageDialog(this, "New password and confirmation do not match!");
            return;
        }

        currentUser.setPassword(newPass);

        User.saveUsers("user.txt", users);

        lblPassword.setText(newPass);

        JOptionPane.showMessageDialog(this, "Password updated successfully!");
        cardLayout.show(pnlCardsProfile, "pnlProfile");
    }//GEN-LAST:event_jButton6ActionPerformed


